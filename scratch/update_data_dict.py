import os

def get_display_width(s):
    # 計算中英文混合字串的顯示寬度 (中文占2格，英文占1格)
    width = 0
    for char in s:
        if ord(char) < 128:
            width += 1
        else:
            width += 2
    return width

def pad_string(s, target_width, align='left'):
    # 根據顯示寬度進行填充
    w = get_display_width(s)
    pad_len = target_width - w
    if pad_len <= 0:
        return s
    if align == 'left':
        return s + ' ' * pad_len
    elif align == 'right':
        return ' ' * pad_len + s
    else:
        left_pad = pad_len // 2
        right_pad = pad_len - left_pad
        return ' ' * left_pad + s + ' ' * right_pad

